class OverworldMap {
    constructor(config) {
        this.overworld = null;
        this.gameObjects = config.gameObjects || {};
        this.walls = config.walls || {};
        this.zindex = config.zindex || {};
        this.cutsceneSpaces = config.cutsceneSpaces || {};

        //lower layer of the map, gameobjects should be visible
        this.lowerImage = new Image();
        this.lowerImage.src = config.lowerSrc;

        //upper layer of the map like trees etc, gameobjects should not be visible
        this.upperImage = new Image();
        this.upperImage.src = config.upperSrc;

        this.isCutscenePlaying = false;

        this.displayedNPC = null;
        this.talkButtonRef = null;
    }

    //drawing lower layer of the map
    //drawing context and aplaying person position to adjust the camera
    drawLowerImage(ctx, cameraPerson) {
        ctx.drawImage(
            this.lowerImage,
            utils.withGrid(14) - cameraPerson.x,
            utils.withGrid(10) - cameraPerson.y
        );
    }

    //drawing upper layer of the map
    drawUpperImage(ctx, cameraPerson) {
        ctx.drawImage(
            this.upperImage,
            utils.withGrid(14) - cameraPerson.x,
            utils.withGrid(10) - cameraPerson.y
        );
    }
    //check if hero can walk to the position
    isSpaceTaken(currentX, currentY, direction) {
        const { x, y } = utils.nextPosition(currentX, currentY, direction);
        return this.walls[`${x},${y}`] || false;
    }

    //checking if hero is behind an object
    isHeroBehindObject(currentX, currentY, direction) {
        const { x, y } = utils.nextPosition(currentX, currentY, direction);
        return this.zindex[`${x},${y}`] || false;
    }

    // mountObjects() {
    //     Object.keys(this.gameObjects).forEach((key) => {
    //         let object = this.gameObjects[key];
    //         object.id = key;
    //         object.mount(this);
    //     });
    // }

    mountObjects() {
        Object.keys(this.gameObjects).forEach((key) => {
            let object = this.gameObjects[key];
            if (!object.isMounted) {
                object.id = key;
                object.mount(this);
            }
        });
    }

    async startCutscene(events) {
        this.isCutscenePlaying = true;
        //start a loop of async events
        //await each one
        for (let i = 0; i < events.length; i++) {
            const event = new OverworldEvent({
                event: events[i],
                map: this,
            });
            await event.init();
        }

        this.isCutscenePlaying = false;

        //Reset NPC to do idle behavior
        Object.values(this.gameObjects).forEach((object) =>
            object.doBehaviorEvent(this)
        );
    }

    //check for event for ex. talking
    checkForAction() {
        const hero = this.gameObjects["hero"]; //current player
        const nextCoords = utils.nextPosition(hero.x, hero.y, hero.direction); //check if hero is next to an npc
        const matchNPC = Object.values(this.gameObjects).find((object) => {
            return (
                `${object.x}, ${object.y}` ===
                `${nextCoords.x}, ${nextCoords.y}`
            );
        });
        //talking event
        if (!this.isCutscenePlaying && matchNPC && matchNPC.talking.length) {
            this.removeTalkButton();
            this.displayedNPC = null;
            this.startCutscene(matchNPC.talking[0].events);
        }
        //check for the same position as item
        const matchItem = Object.values(this.gameObjects).find((object) => {
            return (
                object !== hero && object.x === hero.x && object.y === hero.y
            );
        });
        //pickUp item event
        if (!this.isCutscenePlaying && matchItem && matchItem.isItem) {
            let wasItPicked = matchItem.grab(matchItem);
            if (wasItPicked) {
                this.removeGameObject(matchItem.id);
            }
        }
        // console.log(match);
    }

    //dropping item
    drop(item) {
        const hero = this.gameObjects["hero"];
        console.log(item)
        // Ensure the item and its details are defined
        if (item && item.itemDetails) {
            const path = itemTypeParser(item.itemDetails.itemType);
    
            let counter = 1;
            let name = `item${counter}`;
    
            // Check for existing names and increment counter if needed
            while (this.gameObjects[name]) {
                counter++;
                name = `item${counter}`;
            }
    
            let placeItem = new Item({
                isPlayerControlled: false,
                id: name,
                x: hero.x,
                y: hero.y,
                itemId: item.itemDetails.id,
                itemType: item.itemDetails.itemType,
                //change sprite
                src: `${path}/${item.itemDetails.spriteURL}`,
                isMounted: true,
            });
            // Add the new item to the game objects
            this.gameObjects[name] = placeItem;
        }
        else{
            console.log("itemDetails not exists?")
        }
    }
    

    //adding game object
    addGameObject(gameObject) {
        console.log(gameObject);
        this.gameObjects[gameObject.id] = gameObject;
        gameObject.mount(this);
    }

    //removing game object
    removeGameObject(id) {
        if (this.gameObjects[id]) {
            delete this.gameObjects[id];
            console.log(`GameObject with ID ${id} removed.`);
        } else {
            console.log(`GameObject with ID ${id} not found.`);
        }
    }

    //check if npc is nearby
    checkForNpc() {
        const hero = this.gameObjects["hero"];
        const nextCoords = utils.nextPosition(hero.x, hero.y, hero.direction);

        const nearbyObject = Object.values(this.gameObjects).find((object) => {
            if (object.id !== "hero" && !(object instanceof Item)) {
                const distance = Math.sqrt(
                    Math.pow(object.x - nextCoords.x, 2) +
                        Math.pow(object.y - nextCoords.y, 2)
                );
                if (distance <= 32) {
                    console.log(`Hero position: (${hero.x}, ${hero.y})`);
                    console.log(`NPC position: (${object.x}, ${object.y})`);
                    return true; // Object is within the specified distance
                }
            }
            return false;
        });

        // Check if it's an NPC
        if (
            !this.isCutscenePlaying &&
            nearbyObject &&
            nearbyObject.talking &&
            nearbyObject.talking.length > 0
        ) {
            if (this.displayedNPC !== nearbyObject.id) {
                const approachDirection = this.calculateApproachDirection(
                    hero,
                    nearbyObject
                );
                this.displayedNPC = nearbyObject.id;
                this.displayTalkButton(nearbyObject, approachDirection); // Show talk button
            }
        } else {
            if (this.talkButtonRef) {
                // Remove after walking away
                this.removeTalkButton();
                this.displayedNPC = null;
            }
        }
    }

    calculateApproachDirection(hero, npc) {
        const deltaX = npc.x - hero.x;
        const deltaY = npc.y - hero.y;

        // Determine the direction based on relative positions
        if (Math.abs(deltaX) > Math.abs(deltaY)) {
            return deltaX > 0 ? "right" : "left";
        } else {
            return deltaY > 0 ? "down" : "up";
        }
    }

    // Function to display the talk button
    displayTalkButton(match, approachDirection) {
        if (match && match.talking && match.talking.length > 0) {
            const talkButton = document.createElement("button");
            talkButton.innerHTML = "Talk";
            talkButton.classList.add("talk-button");
            const windowWidth = window.innerWidth;
            const windowHeight = window.innerHeight;
            talkButton.style.left = `${windowWidth / 2 - 70}px`;
            talkButton.style.top = `${windowHeight / 2}px`;

            talkButton.onclick = () => {
                this.startCutscene(match.talking[0].events);
                this.removeTalkButton(); // Remove button after click
                this.displayedNPC = null;
            };

            document.body.appendChild(talkButton);
            this.talkButtonRef = talkButton; // Store a reference to the talk button
        }
    }

    // Function to remove the talk button
    removeTalkButton() {
        if (this.talkButtonRef) {
            this.talkButtonRef.remove();
            this.talkButtonRef = null;
        }
    }

    //check for event by entering specific area
    checkForFootstepCutscene() {
        const hero = this.gameObjects["hero"]; //current player
        const match = this.cutsceneSpaces[`${hero.x},${hero.y}`];
        if (!this.isCutscenePlaying && match) {
            this.startCutscene(match[0].events);
        }
        // console.log(match);
    }

    addWall(x, y) {
        this.walls[`${x},${y}`] = true;
    }

    removeWall(x, y) {
        delete this.walls[`${x},${y}`];
    }

    moveWall(OldX, OldY, direction) {
        this.removeWall(OldX, OldY); //removing wall
        const { x, y } = utils.nextPosition(OldX, OldY, direction); //offset of direction, getting new position of the wall
        this.addWall(x, y); //creating the new wall
    }

    itemTypeParser(type) {
        switch (type) {
            case 0:
                return "./assets/primaryWeapons/";
            case 1:
                return "./assets/secondaryWeapons/";
            case 2:
                return "./assets/armors/";
            case 3:
                return "./assets/helmets/";
            case 4:
                return "./assets/boots/";
            case 5:
                return "./assets/gloves/";
            case 6:
                return "./assets/necklaces/";
            case 7:
                return "./assets/rings/";
            case 8:
                return "./assets/consumables/";
            case 9:
                return "./assets/neutrals/";
            case 10:
                return "./assets/quest/";
        }
    }
}
